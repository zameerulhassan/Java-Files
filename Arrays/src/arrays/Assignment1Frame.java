    public void selectButton(JButton button) {
        String playerOne = jTextField1.getText();
        String playerTwo = jTextField2.getText();
        String playerThree = jTextField3.getText();
        if ((!playerOne.equals(playerTwo)) || (!playerOne.equals(playerThree)) || !playerTwo.equals(playerThree)){
            if (button.getText().equals("")) {
                if (xTurn == false && x2Turn == false) {
                    button.setText(playerOne);
                    xTurn = false;
                    x2Turn = true;
                } else if (xTurn == false && x2Turn == true) {
                    button.setText(playerTwo);
                    xTurn = true;
                    x2Turn = false;
                } else {
                    button.setText(playerThree);
                    xTurn = false;
                    x2Turn = false;
                        }
            }
            checkForTheWin();
        } else {
            JOptionPane.showMessageDialog(this, "Player symbols are automatically assigned"
                    + " if the user chooses the same characters. Change the characters or proceed"
                    + " with the computer assigned characters.");          
            jTextField1.setText("x");
            jTextField2.setText("y");
            jTextField3.setText("z");
            
        }
    }
    
    
