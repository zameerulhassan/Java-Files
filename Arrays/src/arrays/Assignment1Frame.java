    public void winCondition(String input) {
        //winning logic- method
        String x = jTextField1.getText();
        String y = jTextField2.getText();
        String z = jTextField3.getText();
        if (input.contains(x+x+x+x)){
            JOptionPane.showMessageDialog(this, "The winner is: " + jLabel1.getText());          
        } else if (input.contains(y+y+y+y)) {
            JOptionPane.showMessageDialog(this, "The winner is: " + jLabel2.getText());
        } else if (input.contains(z+z+z+z)) {
            JOptionPane.showMessageDialog(this, "The winner is: " + jLabel3.getText());
        }
        
    }
    
